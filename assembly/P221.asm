#MAKE_EXE#

DATA SEGMENT
X DB '4572'
Y DB '3'
XY DW 0FFFFH;X*Y
DATA ENDS

STACK SEGMENT PARA STACK
DB 10 DUP (?)
STACK ENDS

CODE SEGMENT
ASSUME DS:DATA,ES:DATA,SS:STACK,CS:CODE
START:
MOV AX,DATA
MOV DS,AX
MOV ES,AX
MOV AX,STACK
MOV SS,AX

;DATA READ IN
MOV CH,Y
AND CH,00FH
MOV CL,4
MOV DH,0
LEA DI,X
LEA SI,XY
L1:
MOV AL,[DI]
AND AL,00FH
MUL CH
ADD AL,DH
CMP AL,20
JGE L2
CMP AL,10
JGE L3
MOV DH,0
JMP L4
L2:
MOV DH,2
SUB AL,20
JMP L4
L3:
MOV DH,1
SUB AL,10
L4:
ADD AL,030H
MOV [SI],AL
INC SI
INC DI
DEC CL
JNZ L1

MOV AH,04CH
INT 021H
CODE ENDS
END START